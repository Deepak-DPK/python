#arithmetic operators 
a=2
b=3
print(a+b) #add
print(a-b) #sub
print(a*b) #mul
print(a/b) #div  results quotient
print(a ** b)  #2*2*2
print(a  % b)  #modulus results remainder
print(a // b)  # result quotient in whole number 

#comparison operators 

print(a == b)
print(a != b)
print(a > b)
print(a < b)

#logical operators 

x= True
