# ======================================================================================================
#                      LIST                      |                      TUPLE
# ======================================================================================================
# Syntax: Square brackets []                     |  Syntax: Round brackets ()
# Example: my_list = [1, "a"]                    |  Example: my_tuple = (1, "a")
# ------------------------------------------------------------------------------------------------------
# Mutability: Mutable - can be changed after     |  Mutability: Immutable - cannot be changed
#             creation.                          |                              after creation.
# ------------------------------------------------------------------------------------------------------
# Performance: Slightly slower due to its        |  Performance: Faster and more memory-
#              dynamic size.                     |               efficient due to its fixed size.
# ------------------------------------------------------------------------------------------------------
# Use Case: For a collection that needs to       |  Use Case: For data that should never
#           change (e.g., a to-do list).         |              change (e.g., coordinates).
# ======================================================================================================